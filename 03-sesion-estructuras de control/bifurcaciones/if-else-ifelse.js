// if ... else + if ... else

let nota = 5;

if (nota === 5) {
    console.log("En hora buena nota sobresaliente");
} else if (nota === 4) {
    console.log("Buen trabajo")
} else if (nota === 3) {
    console.log("Suficiente")
} else if (nota === 2) {
    console.log("Por poco")
} else if (nota === 1) {
    console.log("No estudiaste!, prepárate para la próxima")
} else {
    console.log("Error, ingresa una nota válida")
}
